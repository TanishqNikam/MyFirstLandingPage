package java;

public class switchcase {

    public static void main(String[] args) {
        
        int no = 2;

        switch (no) {
            case 1:
                System.out.println("samosa");
                break;
            case 2: 
                System.out.println("burger");
                break;
            case 3:
                System.out.println("choclate shale");
                break;
            default:
                System.out.println("we woke up");
        }
    }
    
}
